//! Package API endpoints — bundled product offerings.
//!
//! ## Alpha Hunt (Package 1)
//! Pipeline: Input → Sync-O → B-Line G-Score → Sense Cortex → Trade Price
//! For: DeFi traders, Polymarket bettors, Aixbt-grade signal agents
//!
//! ## Protocol Shield (Package 2)
//! Pipeline: Data stream → Sync-O → G-Score batch
//! For: Akash, Walrus, Bittensor, Render, IPFS
//!
//! ## Trust Transaction (Package 3)
//! Pipeline: FICO query → score-gated escrow decision
//! For: Any agents trading $500+ with unknown counterparts
//!
//! ## What the doc missed
//!
//! The doc describes packages but doesn't note that Alpha Hunt is just
//! Sync-O + Cortex chained in one HTTP call. The efficiency gain is:
//! 1. One auth check instead of 3
//! 2. One network round-trip instead of 3
//! 3. Bundled pricing at 33% discount vs calling individually
//! 4. The G-score from Sync-O feeds DIRECTLY into Cortex's knowledge context
//!    (data freshness IS a form of knowledge for the attention engine)

