// ── Alpha Hunt (Package 1) ─────────────────────────────────────────────────

#[derive(Debug, Deserialize)]
pub struct AlphaHuntRequest {
    /// The signal or data to analyze (text or base64 data)
    pub signal: String,
    /// Market context: "polymarket" | "defi" | "crypto" | "prediction"
    #[serde(default = "default_market")]
    pub market: String,
    /// Current price being considered (for trade execution context)
    pub price: Option<f64>,
    /// Optional known context to seed the knowledge engine
    #[serde(default)]
    pub known_context: Vec<String>,
}

fn default_market() -> String { "crypto".to_string() }

#[derive(Debug, Serialize)]
pub struct AlphaHuntResponse {
    /// Trading signal: "BUY" | "HOLD" | "SKIP" | "INVESTIGATE"
    pub signal: TradeSignal,
    /// Confidence (0.0-1.0): 1.0 = maximum confidence
    pub confidence: f64,
    /// G-score from freshness oracle (0.0 = stale, 1.0 = novel)
    pub g_score: f32,
    /// Freshness verdict
    pub freshness: FreshnessVerdict,
    /// Current price (verified, not cached)
    pub price: Option<f64>,
    /// Ghost Tokens: knowledge gaps that should be resolved before acting
    pub ghost_tokens: Vec<String>,
    /// Auto-questions generated by Sense Cortex
    pub auto_questions: Vec<String>,
    /// Sync-O data quality score (0.0-1.0)
    pub data_quality: f64,
    /// Bandwidth saved by Sync-O (KB)
    pub bandwidth_saved_kb: f64,
    /// Total cost for Alpha Hunt package (10 VIRTUAL)
    pub virtual_charged: u64,
    /// Processing time in nanoseconds
    pub processing_ns: u64,
}

#[derive(Debug, Serialize)]
pub enum TradeSignal {
    Buy,
    Hold,
    Skip,
    Investigate,
}

#[derive(Debug, Serialize)]
pub enum FreshnessVerdict {
    /// G < 0.20: very fresh signal
    Hot,
    /// G 0.20-0.50: reasonably fresh
    Warm,
    /// G 0.50-0.80: stale, market may have priced it in
    Cooling,
    /// G > 0.80: possibly already priced in
    Stale,
}

const MAX_SIGNAL_LEN: usize = 65_536;   // 64 KB signal text
const MAX_CONTEXT_LEN: usize = 4_096;   // per context item
const MAX_CONTEXT_ITEMS: usize = 50;
const MAX_SHIELD_B64_LEN: usize = 67 * 1024 * 1024; // ~50MB raw
const MAX_SHIELD_BYTES: usize = 50 * 1024 * 1024;

pub async fn handle_alpha_hunt(
    State(state): State<AppState>,
    Extension(CallerDid(did)): Extension<CallerDid>,
    Json(req): Json<AlphaHuntRequest>,
) -> Result<Json<AlphaHuntResponse>, (StatusCode, Json<serde_json::Value>)> {
    let t_start = std::time::Instant::now();

    // Validate input sizes
    if req.signal.len() > MAX_SIGNAL_LEN {
        return Err((StatusCode::BAD_REQUEST, Json(json!({
            "error": "signal_too_long",
            "max_chars": MAX_SIGNAL_LEN
        }))));
    }
    if req.known_context.len() > MAX_CONTEXT_ITEMS {
        return Err((StatusCode::BAD_REQUEST, Json(json!({
            "error": "too_many_context_items",
            "max_items": MAX_CONTEXT_ITEMS
        }))));
    }
    for ctx in &req.known_context {
        if ctx.len() > MAX_CONTEXT_LEN {
            return Err((StatusCode::BAD_REQUEST, Json(json!({
                "error": "context_item_too_long",
                "max_chars": MAX_CONTEXT_LEN
            }))));
        }
    }

    // Pre-charge 10 VIRTUAL BEFORE expensive computation (C32: billing bypass prevention)
    let virtual_charged = packages::ALPHA_HUNT_PER_CALL;
    state.deduct_balance(&did, virtual_charged).await.map_err(|avail| (
        StatusCode::PAYMENT_REQUIRED,
        Json(json!({"error": "insufficient_balance", "required": virtual_charged, "available": avail,
                    "message": "Need 10 VIRTUAL for Alpha Hunt package."})),
    ))?;

    // Step 1: Sync-O — compute data entropy as freshness proxy
    let data_bytes = req.signal.as_bytes();
    let byte_entropy = compute_entropy(data_bytes);

    // Step 2: G-score for freshness (Sync-O B-Line equivalent)
    // Low entropy = repetitive/stale data, High entropy = novel information
    let raw_g = (1.0 - byte_entropy) as f32;

    // Step 3: Sense Cortex — run QKV-G attention on the signal
    let mut attention_cache = state.attention_cache.write().await;
    // C37: Evict oldest entry when cache is at capacity (mirrors cortex.rs OOM protection)
    if attention_cache.len() >= crate::gateway::state::MAX_ATTENTION_CACHE_ENTRIES
        && !attention_cache.contains_key(&did)
    {
        if let Some(evict_key) = attention_cache.keys().next().cloned() {
            attention_cache.remove(&evict_key);
        }
    }
    let (engine, seq_idx) = attention_cache
        .entry(did.clone())
        .or_insert_with(|| {
            let mut eng = helm_engine::HelmAttentionEngine::new(128);
            let idx = eng.create_sequence(0);
            (eng, idx)
        });

    // Seed with known context
    use sha2::Digest;
    for (pos, ctx) in req.known_context.iter().enumerate() {
        let mut hasher = sha2::Sha256::new();
        hasher.update(ctx.as_bytes());
        let hash = hasher.finalize();
        let key: Vec<f32> = (0..64).map(|i| {
            let b = hash[i % 32] as f32;
            b / 128.0 - 1.0
        }).collect();
        let value = key.clone();
        let _ = engine.store_kv(*seq_idx, pos * 8, key, value);
    }

    // Query vector from signal
    let mut hasher = sha2::Sha256::new();
    hasher.update(data_bytes);
    let query_hash = hasher.finalize();
    let query_vec: Vec<f32> = (0..64).map(|i| {
        let b = query_hash[i % 32] as f32;
        b / 128.0 - 1.0
    }).collect();

    let attention_result = engine.forward(*seq_idx, &query_vec).unwrap_or(
        helm_engine::AttentionOutput::GapDetected {
            g_metric: 0.5,
            missing_intent: query_vec.clone(),
            closest_block: None,
        }
    );
    drop(attention_cache);

    let (cortex_g, ghost_tokens, missing_intent_opt) = match &attention_result {
        helm_engine::AttentionOutput::Success { g_metric, .. } => (*g_metric, vec![], None),
        helm_engine::AttentionOutput::GapDetected { g_metric, missing_intent, .. } => {
            let tokens = generate_ghost_tokens(missing_intent);
            (*g_metric, tokens, Some(missing_intent.clone()))
        }
    };

    // Combined G-score: blend freshness (raw_g) with cortex G (cortex_g)
    let combined_g = (raw_g * 0.4 + cortex_g * 0.6).min(1.0);

    // Step 4: Generate trade signal
    let signal = if cortex_g < 0.30 && byte_entropy > 0.60 {
        TradeSignal::Buy
    } else if cortex_g < 0.50 {
        TradeSignal::Hold
    } else if !ghost_tokens.is_empty() {
        TradeSignal::Investigate
    } else {
        TradeSignal::Skip
    };

    let confidence = (1.0 - combined_g as f64).max(0.0);

    let freshness = match combined_g {
        g if g < 0.20 => FreshnessVerdict::Hot,
        g if g < 0.50 => FreshnessVerdict::Warm,
        g if g < 0.80 => FreshnessVerdict::Cooling,
        _ => FreshnessVerdict::Stale,
    };

    // Auto-questions from ghost tokens
    let auto_questions: Vec<String> = ghost_tokens.iter().map(|gt| {
        let domain = gt.trim_start_matches("[MISSING: ").trim_end_matches(']');
        match domain {
            "DEFI_SIGNAL" => "What are current DeFi market conditions?",
            "MACRO_EVENT" => "Any macro events affecting this market?",
            "ONCHAIN_METRIC" => "What are current TVL and whale movement signals?",
            _ => "Gather additional market context before acting.",
        }.to_string()
    }).collect();

    // Bandwidth saved: compare entropy-compressed size vs raw
    let estimated_compressed = (data_bytes.len() as f64 * byte_entropy).max(1.0);
    let bandwidth_saved_kb = (data_bytes.len() as f64 - estimated_compressed) / 1024.0;

    let processing_ns = t_start.elapsed().as_nanos() as u64;

    // virtual_charged was pre-computed and deducted above (C32)
    state.record_api_call(&did, "package/alpha-hunt", virtual_charged).await;

    Ok(Json(AlphaHuntResponse {
        signal,
        confidence,
        g_score: combined_g,
        freshness,
        price: req.price,
        ghost_tokens,
        auto_questions,
        data_quality: byte_entropy,
        bandwidth_saved_kb: bandwidth_saved_kb.max(0.0),
        virtual_charged,
        processing_ns,
    }))
}

// ── Protocol Shield (Package 2) ────────────────────────────────────────────

#[derive(Debug, Deserialize)]
pub struct ProtocolShieldRequest {
    /// Protocol name: "akash" | "walrus" | "bittensor" | "render" | "ipfs" | "ionet"
    pub protocol: String,
    /// Base64-encoded data stream to sanitize
    pub data_b64: String,
    /// Batch size for G-score computation
    #[serde(default = "default_batch")]
    pub batch_size: usize,
}

fn default_batch() -> usize { 100 }

#[derive(Debug, Serialize)]
pub struct ProtocolShieldResponse {
    pub protocol: String,
    pub clean_ratio: f64,
    pub processing_ns: u64,
    pub bandwidth_saved_kb: f64,
    pub g_scores: Vec<f32>,
    pub virtual_charged: u64,
    pub recommendation: String,
}

pub async fn handle_protocol_shield(
    State(state): State<AppState>,
    Extension(CallerDid(did)): Extension<CallerDid>,
    Json(req): Json<ProtocolShieldRequest>,
) -> Result<Json<ProtocolShieldResponse>, (StatusCode, Json<serde_json::Value>)> {
    let t_start = std::time::Instant::now();

    if req.data_b64.len() > MAX_SHIELD_B64_LEN {
        return Err((StatusCode::PAYLOAD_TOO_LARGE, Json(json!({
            "error": "data_too_large",
            "max_bytes": MAX_SHIELD_BYTES
        }))));
    }

    use base64::Engine;
    let raw_data = base64::engine::general_purpose::STANDARD
        .decode(&req.data_b64)
        .map_err(|_| (StatusCode::BAD_REQUEST, Json(json!({"error": "invalid_base64"}))))?;

    if raw_data.len() > MAX_SHIELD_BYTES {
        return Err((StatusCode::PAYLOAD_TOO_LARGE, Json(json!({
            "error": "data_too_large",
            "max_bytes": MAX_SHIELD_BYTES
        }))));
    }

    // Pre-charge BEFORE GRG processing (C33: billing bypass prevention).
    // Fee is computed from raw_data size (known after decode, before costly pipeline).
    let virtual_charged = packages::PROTOCOL_SHIELD_PER_MB
        .saturating_mul((raw_data.len() as u64).max(1) / 1024 + 1);
    let virtual_charged = virtual_charged.min(10 * VIRTUAL_UNIT);
    state.deduct_balance(&did, virtual_charged).await.map_err(|avail| (
        StatusCode::PAYMENT_REQUIRED,
        Json(json!({"error": "insufficient_balance", "required": virtual_charged, "available": avail,
                    "message": "Insufficient VIRTUAL balance for Protocol Shield."})),
    ))?;

    // Run GRG pipeline (Sync-O)
    let pipeline = helm_engine::GrgPipeline::new(helm_engine::GrgMode::Safety)
        .with_data_shards(4);

    let encoded = pipeline.encode(&raw_data).map_err(|e| (
        StatusCode::INTERNAL_SERVER_ERROR,
        Json(json!({"error": "synco_failed", "message": e.to_string()})),
    ))?;

    let batch_size = req.batch_size.min(encoded.shards.len()).max(1);
    let entropy = compute_entropy(&raw_data);

    // G-scores per shard (entropy-based approximation)
    let g_scores: Vec<f32> = encoded.shards.iter()
        .take(batch_size)
        .map(|s| compute_entropy(&s.data) as f32)
        .collect();

    let clean_ratio = g_scores.iter().filter(|&&g| g > 0.3).count() as f64 / g_scores.len() as f64;
    let bandwidth_saved_kb = (raw_data.len() as f64 * (1.0 - entropy)) / 1024.0;
    let processing_ns = t_start.elapsed().as_nanos() as u64;

    // virtual_charged was pre-computed and deducted above (C33)
    state.record_api_call(&did, "package/protocol-shield", virtual_charged).await;

    let recommendation = if clean_ratio > 0.8 {
        format!("Data quality is HIGH ({:.0}% clean). Safe to process.", clean_ratio * 100.0)
    } else if clean_ratio > 0.5 {
        format!("Data quality is MEDIUM ({:.0}% clean). Consider filtering duplicates first.", clean_ratio * 100.0)
    } else {
        format!("Data quality is LOW ({:.0}% clean). Significant deduplication recommended.", clean_ratio * 100.0)
    };

    Ok(Json(ProtocolShieldResponse {
        protocol: req.protocol,
        clean_ratio,
        processing_ns,
        bandwidth_saved_kb: bandwidth_saved_kb.max(0.0),
        g_scores,
        virtual_charged,
        recommendation,
    }))
}

fn compute_entropy(data: &[u8]) -> f64 {
    if data.is_empty() { return 0.0; }
    let mut freq = [0u64; 256];
    for &b in data { freq[b as usize] += 1; }
    let n = data.len() as f64;
    let entropy: f64 = freq.iter().filter(|&&c| c > 0).map(|&c| {
        let p = c as f64 / n;
        -p * p.log2()
    }).sum();
    (entropy / 8.0).min(1.0)
}
